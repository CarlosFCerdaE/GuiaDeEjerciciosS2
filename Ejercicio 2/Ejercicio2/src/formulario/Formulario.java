    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        int id = Integer.parseInt(jTFId.getText());
        String nombre = jTFNombre.getText();
        String descripcion = jTFDesc.getText();
        double precio = Double.parseDouble(jTFPrecio.getText());
        
        if (i<max){
            Libro lib = new Libro (id,nombre,descripcion,precio);
            libros[i]=lib;
            i++;
            limpiar();
             JOptionPane.showMessageDialog(this,"Producto Guardado","Guardar",JOptionPane.INFORMATION_MESSAGE);
             mostrarRegistros();
        }
        else{
        JOptionPane.showMessageDialog(this,"No se puede agregar mÃ¡s productos","Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed
 
    private void limpiar(){
        jTFId.setText("");
        jTFNombre.setText("");
        jTFDesc.setText("");
        jTFPrecio.setText("");
        jTFId.requestFocus();
    }
    
    private void mostrarRegistros(){
    
        String reg = "";
        for (int c = 0;c<i;c++){
        
            reg+=libros[c].toString();
        }
        jTaRegistros.setText(reg);
    }
    
